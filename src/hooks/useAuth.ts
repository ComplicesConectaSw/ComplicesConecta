import { useState, useEffect, useCallback, useRef } from 'react';
import { User, Session } from '@supabase/supabase-js';
import { supabase } from '@/integrations/supabase/client';
import { useNavigate } from 'react-router-dom';
import { 
  getAppConfig, 
  DEMO_CREDENTIALS, 
  getDemoPassword, 
  handleDemoAuth, 
  clearDemoAuth, 
  checkDemoSession,
  isProductionAdmin,
  isDemoMode,
  shouldUseRealSupabase
} from '@/lib/app-config';
import { useProfile } from '@/hooks/useProfileCache';
import { StorageManager } from '@/lib/storage-manager';

interface Profile {
  id: string;
  first_name?: string | null;
  last_name?: string | null;
  display_name?: string | null;
  age?: number | null;
  role?: string;
  email?: string | null;
  profile_type?: string | null;
  is_demo?: boolean | null;
  is_verified?: boolean | null;
  is_premium?: boolean | null;
  [key: string]: unknown;
}

interface AuthState {
  user: User | null;
  session: Session | null;
  loading: boolean;
  profile: Profile | null;
}

export const useAuth = () => {
  const [user, setUser] = useState<User | null>(null);
  const [session, setSession] = useState<Session | null>(null);
  const [loading, setLoading] = useState(true);
  const [profile, setProfile] = useState<any>(null);
  const config = getAppConfig();
  const initialized = useRef(false);
  const profileLoaded = useRef(false);

  // Funci√≥n para cargar perfil
  // Usar React Query para cargar perfil con cache
  const { data: cachedProfile, isLoading: profileLoading, error: profileError } = useProfile(user?.id || null);
  
  const loadProfile = useCallback(async (userId: string) => {
    if (profileLoaded.current) {
      console.log('‚ö†Ô∏è Perfil ya cargado, evitando recarga:', userId);
      return;
    }
    
    // Si tenemos datos del cache, usarlos directamente
    if (cachedProfile) {
      console.log('‚úÖ Perfil cargado desde cache:', cachedProfile.first_name);
      setProfile(cachedProfile);
      profileLoaded.current = true;
      return;
    }
    
    try {
      console.log('üîç Cargando perfil para usuario:', userId);
      
      const { data, error } = await supabase
        .from('profiles')
        .select('*')
        .eq('id', userId)
        .single();
      
      console.log('üîç Consulta ejecutada - userId:', userId);
      console.log('üîç Resultado data:', data);
      console.log('üîç Error (si existe):', error);
      
      if (error) {
        console.error('‚ùå Error fetching profile:', error);
        // Si no se encuentra el perfil, crear uno b√°sico
        if (error.code === 'PGRST116') {
          console.log('üÜÜ Perfil no encontrado - creando perfil b√°sico');
          const basicProfile = {
            id: userId,
            user_id: userId,
            first_name: 'Usuario',
            role: 'user',
            is_demo: false,
            created_at: new Date().toISOString(),
            updated_at: new Date().toISOString()
          };
          setProfile(basicProfile);
        }
        return;
      }
      
      if (data) {
        // Manejar tanto array como objeto √∫nico
        const profileData = Array.isArray(data) ? data[0] : data;
        
        console.log('üìã Contenido detallado del perfil:');
        console.log('  - Es array:', Array.isArray(data));
        console.log('  - id:', profileData?.id);
        console.log('  - first_name:', profileData?.first_name);
        console.log('  - last_name:', profileData?.last_name);
        console.log('  - display_name:', (profileData as any)?.display_name);
        console.log('  - role:', (profileData as any)?.role);
        console.log('  - email:', (profileData as any)?.email);
        console.log('  - Objeto completo:', JSON.stringify(data, null, 2));
        
        console.log('‚úÖ Perfil real cargado:', profileData.first_name);
        console.log('üìã Datos completos del perfil:', profileData);
        profileLoaded.current = true;
        setProfile(profileData);
        
        // PERFIL CARGADO - Redirecci√≥n autom√°tica al perfil para usuarios especiales
        console.log('üîç Perfil cargado - ID:', profileData?.id);
        
        // Redirecci√≥n autom√°tica al perfil despu√©s de cargar datos
        if (profileData?.first_name === 'Apoyo' && window.location.pathname === '/') {
          console.log('üîÑ Redirigiendo usuario Apoyo al perfil...');
          setTimeout(() => {
            window.location.href = '/profile-single';
          }, 1000);
        } else {
          console.log('‚úÖ Usuario especial autenticado - usar navegaci√≥n manual');
        }
      } else {
        console.log('‚ö†Ô∏è No se encontr√≥ perfil para el usuario:', userId);
        setProfile(null);
      }
    } catch (error) {
      console.error('‚ùå Error in loadProfile:', error);
      setProfile(null);
    }
  }, [cachedProfile]);

  useEffect(() => {
    if (initialized.current) return;
    initialized.current = true;
    
    console.log('üîÑ Inicializando useAuth en modo:', config.mode);
    
    // Verificar sesi√≥n demo existente al cargar
    // NOTA: checkDemoSession ahora retorna null para forzar recreaci√≥n
    // Los datos demo ya no se persisten en localStorage
    
    // Migrar datos legacy y verificar sesi√≥n usando StorageManager
    StorageManager.migrateToSupabase();
    const sessionFlags = StorageManager.getSessionFlags();
    
    if (sessionFlags.apoyo_authenticated) {
      console.log('üõ°Ô∏è Usuario especial detectado - cargando desde Supabase...');
      // Datos se cargan exclusivamente desde Supabase via React Query
      
      // Reset profileLoaded para permitir carga desde Supabase
      profileLoaded.current = false;
    }
    
    // Solo configurar Supabase si debemos usar conexi√≥n real
    if (shouldUseRealSupabase()) {
      console.log('üîó Configurando autenticaci√≥n Supabase real...');
      
      // Obtener sesi√≥n actual de Supabase
      supabase.auth.getSession().then(({ data: { session } }) => {
        setSession(session);
        setUser(session?.user ?? null);
        if (session?.user) {
          loadProfile(session.user.id);
        }
        setLoading(false);
      });
      
      // DESHABILITAR onAuthStateChange para prevenir logout autom√°tico
      console.log('üö´ onAuthStateChange DESHABILITADO para prevenir auto-logout');
      
      // Solo mantener la sesi√≥n inicial, sin escuchar cambios
      const subscription = { unsubscribe: () => {} };
      
      return () => subscription.unsubscribe();
    } else {
      console.log('üé≠ Modo demo - Supabase deshabilitado');
      setLoading(false);
    }
  }, [loadProfile]);


  const signOut = async () => {
    try {
      console.log('üö™ Cerrando sesi√≥n...');
      
      // Verificar si es sesi√≥n demo usando StorageManager
      const sessionFlags = StorageManager.getSessionFlags();
      
      if (sessionFlags.demo_authenticated) {
        // Limpiar sesi√≥n demo
        clearDemoAuth();
        console.log('‚úÖ Sesi√≥n demo cerrada');
      } else {
        // Cerrar sesi√≥n real de Supabase
        console.log('üîó Cerrando sesi√≥n real de Supabase...');
        const { error } = await supabase.auth.signOut();
        if (error) {
          console.error('‚ùå Error cerrando sesi√≥n:', error);
        } else {
          console.log('‚úÖ Sesi√≥n real cerrada');
        }
      }
      
      // Limpiar estado local
      setUser(null);
      setSession(null);
      setProfile(null);
    } catch (error) {
      console.error('‚ùå Error en signOut:', error);
    }
  };

  const signIn = async (email: string, password: string, accountType: string = 'single') => {
    try {
      setLoading(true);
      console.log('üîê Intentando iniciar sesi√≥n:', email, 'Modo:', config.mode);
      
      // SOLUCI√ìN ESPECIAL para apoyofinancieromexicano@gmail.com
      if (email.toLowerCase() === 'apoyofinancieromexicano@gmail.com') {
        console.log('üõ°Ô∏è Usuario especial detectado - usando autenticaci√≥n personalizada');
        
        if (password !== '123456') {
          throw new Error('Contrase√±a incorrecta');
        }
        
        // Crear sesi√≥n mock persistente
        const mockUser = {
          id: '7c189901-0939-4f28-8d17-4496e0b41492',
          email: 'apoyofinancieromexicano@gmail.com',
          created_at: new Date().toISOString(),
          updated_at: new Date().toISOString(),
          email_confirmed_at: new Date().toISOString(),
          app_metadata: {},
          user_metadata: {}
        };
        
        const mockSession = {
          access_token: 'mock-token-apoyo',
          refresh_token: 'mock-refresh-apoyo',
          expires_in: 3600,
          token_type: 'bearer',
          user: mockUser
        };
        
        // Guardar solo flag de autenticaci√≥n usando StorageManager
        StorageManager.setSessionFlag('apoyo_authenticated', true);
        // ELIMINADO: No almacenar datos de usuario en localStorage
        // Los datos se cargan exclusivamente desde Supabase
        
        setUser(mockUser as any);
        setSession(mockSession as any);
        await loadProfile(mockUser.id);
        console.log('‚úÖ Sesi√≥n personalizada iniciada para usuario especial');
        
        return { user: mockUser, session: mockSession };
      }
      
      // Verificar si es credencial de producci√≥n (complicesconectasw@outlook.es)
      if (isProductionAdmin(email)) {
        console.log('üè¢ Credencial de producci√≥n detectada - limpiando demo y usando Supabase real');
        
        // IMPORTANTE: Limpiar cualquier sesi√≥n demo antes de autenticar producci√≥n
        clearDemoAuth();
        
        const { data, error } = await supabase.auth.signInWithPassword({
          email,
          password,
        });
        
        if (error) throw error;
        
        if (data.user) {
          setUser(data.user);
          setSession(data.session);
          await loadProfile(data.user.id);
          console.log('‚úÖ Sesi√≥n de producci√≥n iniciada para:', email);
        }
        
        return data;
      }
      
      // Verificar si es una credencial demo
      if (DEMO_CREDENTIALS.includes(email)) {
        console.log('üé≠ Credencial demo detectada');
        const demoPassword = getDemoPassword(email);
        
        if (password !== demoPassword) {
          throw new Error('Contrase√±a incorrecta para usuario demo');
        }
        
        // Manejar autenticaci√≥n demo
        const demoAuth = handleDemoAuth(email, accountType);
        if (demoAuth) {
          setUser(demoAuth.user as any);
          setSession(demoAuth.session as any);
          await loadProfile(demoAuth.user.id);
          console.log('‚úÖ Sesi√≥n demo iniciada para:', email);
          return { user: demoAuth.user, session: demoAuth.session };
        }
      }
      
      // En modo producci√≥n, intentar con Supabase para otros usuarios
      if (config.mode === 'production') {
        console.log('üîó Intentando autenticaci√≥n real con Supabase para:', email);
        
        // Limpiar cualquier sesi√≥n demo antes de autenticar
        clearDemoAuth();
        
        const { data, error } = await supabase.auth.signInWithPassword({
          email,
          password,
        });
        
        if (error) {
          console.error('‚ùå Error de autenticaci√≥n Supabase:', error);
          throw error;
        }
        
        if (data.user) {
          console.log('‚úÖ Usuario autenticado con Supabase:', data.user.email);
          setUser(data.user);
          setSession(data.session);
          await loadProfile(data.user.id);
          console.log('‚úÖ Sesi√≥n real iniciada para:', email);
        }
        
        return data;
      }
      
      throw new Error('Credenciales no v√°lidas para el modo actual');
    } catch (error) {
      console.error('‚ùå Error signing in:', error);
      throw error;
    } finally {
      setLoading(false);
    }
  };

  // Funci√≥n para verificar si un usuario es administrador
  const isAdmin = () => {
    // Demo admin check
    const demoAuth = localStorage.getItem('demo_authenticated');
    const demoUser = localStorage.getItem('demo_user');
    
    if (demoAuth === 'true' && demoUser) {
      try {
        const parsedDemoUser = JSON.parse(demoUser);
        const isDemoAdmin = parsedDemoUser.accountType === 'admin' || parsedDemoUser.role === 'admin';
        
        console.log('üé≠ Demo admin check:', {
          email: parsedDemoUser.email,
          accountType: parsedDemoUser.accountType,
          role: parsedDemoUser.role,
          isDemoAdmin
        });
        
        return isDemoAdmin;
      } catch (error) {
        console.error('‚ùå Error parsing demo user:', error);
        return false;
      }
    }
    
    // CR√çTICO: Verificar admin basado en EMAIL DE AUTENTICACI√ìN, no perfil
    const userEmail = user?.email?.toLowerCase();
    
    // Lista de emails admin - INCLUIR djwacko28@gmail.com
    const adminEmails = [
      'admin',                      // Admin demo solamente
      'complicesconectasw@outlook.es',  // Admin principal
      'djwacko28@gmail.com'        // Admin secundario
    ];
    
    // PRIORIDAD: Email de autenticaci√≥n determina admin status
    const isAdminByEmail = userEmail && adminEmails.includes(userEmail);
    
    // SECUNDARIO: Role del perfil (solo si email no es admin)
    const profileRole = profile?.role;
    const isAdminByRole = !isAdminByEmail && profileRole === 'admin';
    
    const isAdminReal = isAdminByEmail || isAdminByRole;
    
    if (userEmail) {
      console.log('üîê Admin real check:', {
        authEmail: userEmail,
        profileEmail: profile?.email,
        profileRole,
        isAdminByEmail,
        isAdminByRole,
        finalResult: isAdminReal
      });
    }
    
    return isAdminReal;
  };

  // Funci√≥n para determinar si debe usar AdminProduction (datos reales)
  const shouldUseProductionAdmin = () => {
    const demoAuth = localStorage.getItem('demo_authenticated');
    const demoUser = localStorage.getItem('demo_user');
    
    // Si es demo admin, usar panel de producci√≥n
    if (demoAuth === 'true' && demoUser) {
      const user = JSON.parse(demoUser);
      return user.accountType === 'admin' || user.role === 'admin';
    }
    
    // Si es admin real (complicesconectasw@outlook.es), usar panel de producci√≥n
    const userEmail = user?.email?.toLowerCase();
    const isRealAdmin = userEmail === 'complicesconectasw@outlook.es';
    
    console.log('üè≠ shouldUseProductionAdmin check:', {
      userEmail,
      isRealAdmin,
      demoAuth: demoAuth === 'true'
    });
    
    return isRealAdmin;
  };

  const isDemo = () => {
    const demoAuth = localStorage.getItem('demo_authenticated');
    const demoUser = localStorage.getItem('demo_user');
    const isDemoActive = demoAuth === 'true' && demoUser;
    
    if (isDemoActive) {
      try {
        const parsedUser = JSON.parse(demoUser);
        console.log('üé≠ Demo mode active for:', parsedUser.email, 'Role:', parsedUser.role);
      } catch (error) {
        console.log('üé≠ Demo mode active but invalid user data');
      }
    }
    
    return isDemoActive;
  };

  const getProfileType = () => {
    return profile?.profile_type || profile?.account_type || 'single';
  };

  const isAuthenticated = () => {
    // Verificar sesi√≥n demo primero
    const demoAuth = localStorage.getItem('demo_authenticated');
    const demoUser = localStorage.getItem('demo_user');
    if (demoAuth === 'true' && demoUser) {
      console.log('‚úÖ Authenticated via demo session');
      return true;
    }
    
    // Verificar autenticaci√≥n real
    const realAuth = !!user && !!session;
    if (realAuth) {
      console.log('‚úÖ Authenticated via real Supabase session');
    }
    
    return realAuth;
  };

  return {
    user,
    session,
    profile,
    loading,
    signIn,
    signOut,
    isAdmin,
    isDemo,
    isAuthenticated,
    getProfileType,
    loadProfile,
    // Nuevas funciones de utilidad
    isDemoMode: isDemoMode,
    shouldUseRealSupabase: shouldUseRealSupabase,
    shouldUseProductionAdmin,
    appMode: config.mode
  };
};